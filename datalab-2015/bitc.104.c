/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int y) {
	int ans;
	long long x = y; // need 1 more op only using int
	x = x ^ (x << 2);
	ans = (!(x >> 17)) << 4;
	ans ^= 25;
	ans ^= (!(x >> ans)) << 3;
	++ans; // ans ^= 4
	++ans;
	++ans;
	++ans;
	ans ^= (!(x >> ans)) << 2;
	ans += ((~0x5B) >> ((x >> ans) & 30)) & 3;
	return ans;
}
